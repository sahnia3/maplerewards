/** Order of letters in "maple". */
export const MAPLE_ORDER: ReadonlyArray<keyof typeof LETTERS> = [
  "m",
  "a",
  "p",
  "l",
  "e",
];

/** Pre-computed cumulative x-positions for each letter in "maple" (with KERN). */
export const MAPLE_POSITIONS: ReadonlyArray<{
  key: keyof typeof LETTERS;
  x: number;
}> = (() => {
  const out: Array<{ key: keyof typeof LETTERS; x: number }> = [];
  let x = 0;
  for (const k of MAPLE_ORDER) {
    out.push({ key: k, x });
    x += LETTERS[k].advance - KERN;
  }
  return out;
})();

/** Width of the rendered "maple" wordmark (last letter's advance included). */
export const MAPLE_WIDTH: number = MAPLE_POSITIONS.reduce(
  (_acc, { x }, i) =>
    i === MAPLE_POSITIONS.length - 1
      ? x + LETTERS[MAPLE_POSITIONS[i].key].advance
      : _acc,
  0,
);
